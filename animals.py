def list_animals():
    animals = ['Cat', 'Dog', 'Elephant', 'Frog']
    for animal in animals:
        print(animal)

def list_birds():
    birds = ['Sparrow', 'Eagle', 'Parrot', 'Penguin']
    for bird in birds:
        print(bird)

if __name__ == "__main__":
    list_animals()
    print("\nBirds:")
    list_birds()
